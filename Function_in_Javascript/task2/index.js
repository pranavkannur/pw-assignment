// Function to generate personalized greeting
function generateGreeting(name) {
    return `Hello, ${name}! Hope you're having a fantastic day!`;
}

// Greeting three different people
const greeting1 = generateGreeting("Emily");
const greeting2 = generateGreeting("David");
const greeting3 = generateGreeting("Sophia");

console.log(greeting1); // Output: Hello, Emily! Hope you're having a fantastic day!
console.log(greeting2); // Output: Hello, David! Hope you're having a fantastic day!
console.log(greeting3); // Output: Hello, Sophia! Hope you're having a fantastic day!
